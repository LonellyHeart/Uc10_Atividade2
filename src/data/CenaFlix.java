public class CenaFlix {
    private int id;
    private String nomeFilme;
    private String lancamentoFilme;
    private String categoriaFilme;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNomeFilme() {
        return nomeFilme;
    }

    public void setNomeFilme(String nomeFilme) {
        this.nomeFilme = nomeFilme;
    }

    public String getLancamentoFilme() {
        return lancamentoFilme;
    }

    public void setLancamentoFilme(String lancamentoFilme) {
        this.lancamentoFilme = lancamentoFilme;
    }

    public String getCategoriaFilme() {
        return categoriaFilme;
    }

    public void setCategoriaFilme(String categoriaFilme) {
        this.categoriaFilme = categoriaFilme;
    }
            
}
