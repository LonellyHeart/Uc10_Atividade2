            private void preencherTabela()
                {

                    CenaFlixDao cenaFlixDao = new CenaFlixDao();
                    
                    String nomeFilme = txtBuscaFilme.getText();
                    List<CenaFlix> listaFilmes = cenaFlixDao.getListaFilme(nomeFilme);
                    
                    if (listaFilmes == null || listaFilmes.isEmpty()) {
                       JOptionPane.showMessageDialog(null, "Nenhum filme encontrado no banco de dados.");
                       return;}
                    

                    DefaultTableModel tabelaFilmes = (DefaultTableModel) tblFilmes.getModel();

                    tblFilmes.setRowSorter(new TableRowSorter(tabelaFilmes));

                    tabelaFilmes.setNumRows(0);
                            

                    
                    for (CenaFlix c : listaFilmes) {
                        Object[] obj = new Object[] { 
                            c.getId(),                           
                            c.getNomeFilme(),                     
                            c.getLancamentoFilme(),   
                            c.getCategoriaFilme()                  
                                       
                        };

                        tabelaFilmes.addRow(obj);
                        
                    }    
                 }
            
